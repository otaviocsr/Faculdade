# Exercício 04: Realiza a leitura de 3 floats e imprime a média aritmética.

input1 = float(input("Digite o primeiro número: "))
input2 = float(input("Digite o segundo número: "))
input3 = float(input("Digite o terceiro número: "))

media = (input1 + input2 + input3) / 3

print(f"A média aritmética dos números {input1}, {input2} e {input3} é {media}")