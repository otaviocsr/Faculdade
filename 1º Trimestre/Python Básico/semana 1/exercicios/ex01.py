# Exercício 01: Realiza a leitura de 2 floats e imprime as seguintes operações: soma, subtração, multiplicação, divisão e resto da divisão.

n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))

print("-------------------------------------")

print("Soma: ", n1 + n2)
print("Subtração: ", n1 - n2)
print("Multiplicação: ", n1 * n2)
print("Divisão: ", n1 / n2)
print("Resto da divisão: ", n1 % n2)